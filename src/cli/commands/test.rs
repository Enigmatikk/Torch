//! Testing operations commands

use colored::*;
use std::process::Command;

/// Run tests with optional filters
pub fn run_tests(filter: Option<String>, unit: bool, integration: bool) -> Result<(), Box<dyn std::error::Error>> {
    println!("{} Running Torch tests...", "ðŸ§ª".yellow());
    
    let mut cmd = Command::new("cargo");
    cmd.arg("test");
    
    // Add test type filters
    if unit && !integration {
        cmd.arg("--lib");
        println!("{} Running unit tests only", "ðŸ“".blue());
    } else if integration && !unit {
        cmd.arg("--test");
        cmd.arg("*");
        println!("{} Running integration tests only", "ðŸ”—".blue());
    } else {
        println!("{} Running all tests", "ðŸŽ¯".blue());
    }
    
    // Add filter if provided
    if let Some(test_filter) = filter {
        cmd.arg(&test_filter);
        println!("{} Filter: {}", "ðŸ”".blue(), test_filter.cyan());
    }
    
    // Add additional test arguments
    cmd.arg("--");
    cmd.arg("--nocapture");
    
    println!();
    
    let status = cmd.status()?;
    
    if status.success() {
        println!();
        println!("{} All tests passed!", "âœ…".green().bold());
    } else {
        println!();
        println!("{} Some tests failed", "âŒ".red().bold());
        return Err("Test failures detected".into());
    }
    
    Ok(())
}

/// Generate a new test file
pub fn generate_test(name: &str, unit: bool) -> Result<(), Box<dyn std::error::Error>> {
    println!("{} Generating test: {}", "ðŸ§ª".yellow(), name.cyan().bold());
    
    let test_name = if name.ends_with("Test") {
        name.to_string()
    } else {
        format!("{}Test", name)
    };
    
    let (filename, content) = if unit {
        let filename = format!("src/tests/{}.rs", test_name.to_lowercase());
        let content = generate_unit_test(&test_name);
        (filename, content)
    } else {
        let filename = format!("tests/{}.rs", test_name.to_lowercase());
        let content = generate_integration_test(&test_name);
        (filename, content)
    };
    
    // Create directory if it doesn't exist
    if let Some(parent) = std::path::Path::new(&filename).parent() {
        std::fs::create_dir_all(parent)?;
    }
    
    std::fs::write(&filename, content)?;
    
    println!("{} Test created: {}", "âœ…".green(), filename);
    
    if unit {
        println!("{} Unit test includes: basic test structure", "ðŸ’¡".blue());
    } else {
        println!("{} Integration test includes: full application testing", "ðŸ’¡".blue());
    }
    
    Ok(())
}

